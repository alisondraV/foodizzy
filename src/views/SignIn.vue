<template>
  <div>
    <h1>Welcome!</h1>
    <p>Letâ€™s optimize your food consumption together</p>
    <form @submit.prevent>
      <label>
        Email:
        <input type="email" placeholder="Type in your email" v-model="email" />
      </label>
      <br />
      <label>
        Password:
        <input
          type="password"
          placeholder="Type in your password"
          v-model="password"
        />
      </label>
      <br />
      <v-button label="Sign In" @click="signIn" />
    </form>
    <button @click="signUpThroughGoogle">Sign Up though Google</button>
    <button @click="goToSignUpPage">Sign Up</button>
  </div>
</template>

<script lang="ts">
import { Component, Vue } from "vue-property-decorator";
import Authentication from "@/utils/Authentication";
import router from "@/router";
import VButton from "@/components/VButton.vue";

@Component({
  components: {
    VButton
  }
})
export default class SignIn extends Vue {
  email = "";
  password = "";

  async signIn() {
    await Authentication.signIn(this.email, this.password);
    await router.push("/app-main");
  }

  goToSignUpPage() {
    router.push("/sign-up");
  }

  async signUpThroughGoogle() {
    await Authentication.signUpThroughGoogle();
    await router.push("/home");
  }
}
</script>
